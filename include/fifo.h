#ifndef FIFO_H
#define FIFO_H

#include <stdbool.h>
#include <stdint.h>

/**
 * @file fifo.h
 * @brief Кольцевой FIFO для приёма данных через UART.
 *
 * Модуль рассчитан на схему несколько писателей, один читатель:
 * - запись выполняется из обработчиков прерывания;
 * - чтение и проверка пустоты - из основного потока
 */

/**
 * @brief Инициализирует пустую очередь
 *
 * @note Вызывать один раз из main до первого write_fifo/read_fifo,
 *       желательно до включения прерываний
 */
void fifo_init();

/**
 * @brief Проверяет, пуста ли очередь
 *
 * @retval true  Очередь пуста
 * @retval false В очереди есть хотя бы один байт
 *
 * @note Вызывать из основного потока
 */
bool fifo_is_empty();

/**
 * @brief Извлекает самый старый непрочитанный байт из очереди
 *
 * @param[out] data Указатель на переменную для записи байта.
 *                  Не должен быть NULL
 *
 * @retval true  Байт успешно прочитан
 * @retval false Очередь пуста, *data не изменяется
 *
 * @note Вызывать из основного потока
 */
bool read_fifo(uint8_t *data);

/**
 * @brief Добавляет байт в конец очереди
 *
 * @param[in] data Байт для записи
 *
 * @retval true  Байт успешно записан
 * @retval false Буфер полон, данные не записаны
 *
 * @note Предназначена для вызова из прерывания
 */
bool write_fifo(uint8_t data);

#endif
